static void print_logo_narrow(void) {
 
    /* wpm counter */
    char wpm_str[8];
    oled_set_cursor(0,8);
    sprintf(wpm_str, " %03d", current_wpm);
    oled_write(wpm_str, false);
 
    oled_set_cursor(0,9);
    oled_write(" wpm", false);
}
 
static void print_status_narrow(void) {
 
    if (is_oled_off && current_wpm > 0) {
        oled_on();
        is_oled_off = false;
        anim_sleep = timer_read32();
    } else if(!is_oled_off && timer_elapsed32(anim_sleep) > OLED_TIMEOUT) {
        oled_off();
        is_oled_off = true;
        return;
    } else if (is_oled_off) {
        return;
    }
 
    oled_set_cursor(0,3);
 
    switch (get_highest_layer(default_layer_state)) {
        case _QWERTY:
            oled_write("QWRTY", false);
            break;
        case _GAME:
            oled_write("GAME", false);
            break;
        default:
            oled_write("UNDEF", false);
    }

    oled_set_cursor(0,5);
 
    /* Print current layer */
    oled_write("LAYER", false);
 
    oled_set_cursor(0,6);
 
    switch (get_highest_layer(layer_state)) {
        case _QWERTY:
            oled_write("Base ", false);
            break;
        case _NUMBERS:
            oled_write("Numpad", false);
            break;
        case _SYMB2:
            oled_write("Symbols", false);
            break;
        case _GAME:
            oled_write("Game", false);
            break;
        case _RAISE:
            oled_write("Raise", false);
            break;
        case _LOWER:
            oled_write("Lower", false);
            break;
    }
 
    /* caps lock */
    oled_set_cursor(0,8);
    oled_write("Luna", led_usb_state.caps_lock);
 
    /* KEYBOARD PET RENDER START */
 
    render_luna(0,13);
 
    /* KEYBOARD PET RENDER END */
}

